package org.example;

public class Tipos {
    //psvm apreviação
    public static void main(String[] args) {
        //Declaração de variaveis
        String nome = "Marta";
        int idade = 30;
        double peso = 65.2;

        // Declaração de constates
        //final vira uma constante
        final String CPF = "123.456.789-00";

        // Exibindo resultados
        // concatenando com sinal de "+"
        //sout apreviação
        System.out.println("O nome do usuário é: " + nome);
        System.out.println("O CPF do usuário é: " + CPF);
        System.out.println("A idade do usuário é: " + idade + " anos") ;
        System.out.println("O peso do usuário é: " + peso + " Kg");

    }
}
